'''
Funcion lambda que calcula el cubo de un numero entero
'''
cubo = lambda x: x**3
print(cubo(3))
print("Antonio Moreno Jimenez")

'''
funcion lambda que modifica la salida de una cadena string
'''
salida = lambda x: x.upper()
print(salida("hola mundo"))
